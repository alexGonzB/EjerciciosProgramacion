public class Zona {
    private String nombre;
    private float sueldo;

    public Zona(String nombre, float sueldo) {
        this.nombre = nombre;
        this.sueldo = sueldo;
    }

    public String getNombre() {
        return nombre;
    }

    public float getSueldo() {
        return sueldo;
    }
    
    
}
